print(15 + 4)  # Dit is de som van 15 en 4.
print(15 - 4)  # Dit is het verschil tussen 15 en 4.
print(15 * 4)  # Dit is het product van 15 en 4.
print(15 / 4)  # Dit is de deling van 15 door 4 (met decimaal).
print(15 // 4) # Dit is de gehele deling van 15 door 4 (afgerond naar beneden).
print(15 ** 4) # Dit is 15 tot de macht 4 (15 * 15 * 15 * 15).
print(15 % 4)  # Dit is de rest van de deling van 15 door 4.